#include<stdio.h>
void main()
{
    int arr[5],i,p,j;


    printf("Enter 5 array ele\n");
    for(i=0;i<5;i++)
    {
        scanf("%d",&arr[i]);
    }
      printf(" Before sort array ele\n");
    for(i=0;i<5;i++)
    {
        printf("%d\n",arr[i]);
    }

    for(i=1;i<5;i++)//iterations
    {
        p=arr[i];//pivot
        j=i-1;//pivot - 1 location
        while(p<arr[j] && j!=-1)//(-1) bcz 1 then -1 location.
        {
            arr[j+1]=arr[j];
            j--;
        }
        arr[j+1]=p;

    }

      printf(" After sort array ele\n");
    for(i=0;i<5;i++)
    {
        printf("%d\n",arr[i]);
    }

}
