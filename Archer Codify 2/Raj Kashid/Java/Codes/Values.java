package Codes;

public class Values {
    public static void main(String[] args) {
        int x = 10;
        short s = (short) x;
        byte b = (byte) x;
        double d = 102.120;
        float f = (float) d;
        System.out.println("int --> " + x);
        System.out.println("short --> " + s);
        System.out.println("byte --> " + b);
        System.out.println("float --> " + f);
        System.out.println("double --> " + d);
    }
}
