package Codes;

public class DemoUnary {
    public static void main(String[] args) {
        int x = 10;

        System.out.println("x --> " + x);
        System.out.println("x++ --> " + x++);
        System.out.println("x-- --> " + x--);
        System.out.println("++x --> " + ++x);
        System.out.println("--x --> " + --x);
    }
}
