class InputUsinginit
{
	public static void main(String []args)
	{
		int x=123;
		byte b=10;
		char ch='$';
		double db=56.1133;
		float ft=3.4F;
		boolean d=true;

		System.out.println("x="+x);
		System.out.println("b="+b);
		System.out.println("ch="+ch);
		System.out.println("db="+db);
		System.out.println("ft="+ft);
		System.out.println("d="+d);
	}
};


